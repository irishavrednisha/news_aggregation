def is_not_related_to_russia(
        session,
        cluster: Cluster,
        debug: bool = False
) -> bool:
    """
    True = новость надо убрать как зарубежную без связи с РФ.

    Логика:
    - есть иностранные маркеры и нет российских => убрать;
    - есть иностранные и российские => оставить;
    - нет иностранных => оставить.
    """
    text = cluster_full_text_for_filter(session, cluster)

    foreign_matches = find_marker_matches(text, FOREIGN_MARKERS)
    russian_matches = find_marker_matches(text, RUSSIA_MARKERS)

    has_foreign = len(foreign_matches) > 0
    has_russia = len(russian_matches) > 0

    if debug:
        print("Foreign markers:", foreign_matches[:15])
        print("Russian markers:", russian_matches[:15])
        print("has_foreign:", has_foreign)
        print("has_russia:", has_russia)

    return has_foreign and not has_russia




def normalize_filter_text(text: str) -> str:
    if not text:
        return ""

    text = text.lower().replace("ё", "е")
    text = re.sub(r"[^а-яa-z0-9\-\. ]", " ", text)
    text = re.sub(r"\s+", " ", text)

    return text.strip()


def lemmatize_text(text: str) -> str:
    """
    Приводит русские слова к нормальной форме.
    Английские слова и аббревиатуры оставляет как есть.
    """
    normalized = normalize_filter_text(text)

    if not normalized:
        return ""

    if morph is None:
        return normalized

    result = []

    for token in normalized.split():
        if re.fullmatch(r"[а-я]+", token):
            parsed = morph.parse(token)
            if parsed:
                result.append(parsed[0].normal_form)
            else:
                result.append(token)
        else:
            result.append(token)

    return " ".join(result)


def prepare_markers(markers: list[str]) -> list[str]:
    """
    Лемматизируем маркеры один раз и кэшируем.
    """
    cache_key = tuple(markers)

    if cache_key in _marker_cache:
        return _marker_cache[cache_key]

    prepared = []

    for marker in markers:
        marker = marker.strip()

        if not marker:
            continue

        prepared.append(lemmatize_text(marker))

    # сначала длинные маркеры, потом короткие
    prepared = sorted(set(prepared), key=len, reverse=True)

    _marker_cache[cache_key] = prepared

    return prepared


def contains_any_marker(text: str, markers: list[str]) -> bool:
    normalized_text = lemmatize_text(text)
    prepared_markers = prepare_markers(markers)

    return any(marker in normalized_text for marker in prepared_markers)


def find_marker_matches(text: str, markers: list[str]) -> list[str]:
    """
    Удобно для debug: показывает, какие маркеры реально сработали.
    """
    normalized_text = lemmatize_text(text)
    prepared_markers = prepare_markers(markers)

    return [
        marker for marker in prepared_markers
        if marker in normalized_text
    ]


def is_foreign_news_by_title(cluster: Cluster) -> bool:
    """
    Проверяет только заголовок.

    Важно:
    - если есть иностранный маркер, но одновременно есть российский контекст,
      новость НЕ отбрасываем;
    - отбрасываем только явно зарубежные заголовки без связи с РФ.
    """
    title = cluster.title or ""

    has_foreign_marker = contains_any_marker(title, FOREIGN_TITLE_MARKERS)
    has_russian_marker = contains_any_marker(title, RUSSIAN_CONTEXT_MARKERS)

    return has_foreign_marker and not has_russian_marker

def keyword_penalty_for_tag(text: str, tag: str) -> float:
    negative_keywords = TAG_NEGATIVE_KEYWORDS.get(tag, [])

    if not negative_keywords:
        return 0.0

    matches = count_keyword_matches(text, negative_keywords)

    if matches == 0:
        return 0.0

    return min(0.05 * matches, 0.15)

def is_not_publication_by_keywords(text: str) -> bool:
    normalized_text = lemmatize_text(text)

    for keyword in NOT_PUBLICATION_KEYWORDS:
        normalized_keyword = lemmatize_text(keyword)

        if normalized_keyword in normalized_text:
            return True

    return False

def cluster_has_content(session, cluster_id: int) -> bool:
    news_items = (
        session.query(News)
        .filter(News.cluster_id == cluster_id)
        .all()
    )

    if not news_items:
        return False

    for news in news_items:
        if news.title and news.text and len(news.text.strip()) > 200:
            return True

    return False