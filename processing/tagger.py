_tag_embeddings_cache = None


def cosine_sim(vec1, vec2) -> float:
    if vec1 is None or vec2 is None:
        return 0.0

    a = np.array(vec1, dtype=float).reshape(1, -1)
    b = np.array(vec2, dtype=float).reshape(1, -1)

    return float(cosine_similarity(a, b)[0][0])


def get_tag_embeddings() -> dict[str, list[float]]:
    """
    Строит embedding для описаний тегов.
    Кэшируется, чтобы не пересчитывать на каждом кластере.
    """
    global _tag_embeddings_cache

    if _tag_embeddings_cache is not None:
        return _tag_embeddings_cache

    result = {}

    for tag, description in TAGS.items():
        result[tag] = build_embedding(description)

    _tag_embeddings_cache = result

    return result


def cut_text(text: str, max_chars: int) -> str:
    """
    Обрезает длинный текст до max_chars символов.
    Это нужно, чтобы не передавать в embedding-модель слишком большой текст.
    """
    if not text:
        return ""

    text = re.sub(r"\s+", " ", text).strip()

    if len(text) <= max_chars:
        return text

    return text[:max_chars].strip()


def get_cluster_text_for_tagging(session, cluster: Cluster) -> str:
    """
    Формирует текст для тематического тегирования кластера.

    Используем не только summary, но и полный текст статей:
    - заголовок кластера имеет самый большой вес;
    - summary тоже усиливается, потому что отражает основное событие;
    - полный текст основной новости даёт отраслевые термины;
    - остальные новости кластера добавляются как дополнительный контекст.

    Повторение заголовка и summary — простой способ повысить их вес
    без изменения всей логики score_tags_detailed().
    """

    parts = []

    if cluster.title:
        title = cut_text(cluster.title, 500)

        # Заголовок самый важный, поэтому добавляем несколько раз
        parts.append(title)
        parts.append(title)
        parts.append(title)

    if cluster.summary_text:
        summary = cut_text(cluster.summary_text, 1500)

        # Summary важнее обычного полного текста
        parts.append(summary)
        parts.append(summary)

    news_items = (
        session.query(News)
        .filter(News.cluster_id == cluster.id)
        .order_by(
            News.is_primary.desc(),
            News.published_at.asc().nullslast(),
            News.collected_at.asc()
        )
        .all()
    )

    for index, news in enumerate(news_items[:5]):
        if news.title:
            news_title = cut_text(news.title, 500)

            # Заголовки новостей внутри кластера тоже полезны
            parts.append(news_title)
            parts.append(news_title)

        if news.text:
            if index == 0 or news.is_primary:
                # Основную новость берём почти полностью
                parts.append(cut_text(news.text, 5000))
            else:
                # Остальные новости добавляем короче,
                # чтобы не забить текст повторами и служебными фрагментами
                parts.append(cut_text(news.text, 2500))

    return "\n".join(part for part in parts if part)

def score_tags(text: str) -> list[tuple[str, float]]:
    """
    Возвращает все теги с итоговым score.

    Итоговый score = embedding similarity + keyword boost.
    """
    if not text:
        return []

    text_embedding = build_embedding(text)
    tag_embeddings = get_tag_embeddings()

    scored_tags = []

    for tag, tag_embedding in tag_embeddings.items():
        embedding_score = cosine_sim(text_embedding, tag_embedding)
        boost = keyword_boost_for_tag(text, tag)

        final_score = embedding_score + boost

        scored_tags.append((tag, final_score))

    scored_tags.sort(key=lambda item: item[1], reverse=True)

    return scored_tags


def define_cluster_tags(
    text: str,
    min_score: float = 0.38,
    max_tags: int = 2
) -> list[str]:
    """
    Определяет теги по embedding-сходству.
    """
    scored_tags = score_tags(text)

    selected_tags = [
        tag for tag, score in scored_tags
        if score >= min_score
    ]

    return selected_tags[:max_tags]



def tag_cluster(
    session,
    cluster_id: int,
    min_score: float = 0.38,
    max_tags: int = 3,
    debug: bool = True
) -> list[str]:
    cluster = session.query(Cluster).filter(Cluster.id == cluster_id).first()

    if not cluster:
        return []

    if not cluster.summary_text or not cluster.summary_text.strip():
        cluster.tags = None
        cluster.status = "not_for_publication"
        session.commit()

    if is_not_related_to_russia(session, cluster, debug=debug):
        cluster.tags = None
        cluster.status = "not_for_publication"
        session.commit()

        if debug:
            print("-" * 100)
            print(f"Кластер #{cluster.id}")
            print(f"Заголовок: {cluster.title}")
            print("Статус: not_for_publication")
            print("Причина: зарубежная новость без связи с РФ")

        return []

    text = get_cluster_text_for_tagging(session, cluster)

    if not cluster_has_content(session, cluster.id):
        cluster.tags = None
        cluster.status = "not_for_publication"

        session.commit()

        if debug:
            print("-" * 100)
            print(f"Кластер #{cluster.id}")
            print("Статус: not_for_publication")
            print("Причина: отсутствует полноценный текст")

        return []

    if is_not_publication_by_keywords(text):
        cluster.tags = None
        cluster.status = "not_for_publication"
        session.commit()

        if debug:
            print("-" * 100)
            print(f"Кластер #{cluster.id}")
            print(f"Заголовок: {cluster.title}")
            print("Статус: not_for_publication")
            print("Причина: стоп-слова нерелевантной темы")

        return []

    scored_tags = score_tags_detailed(text)

    selected_tags = select_tags_by_score_gap(
        scored_tags=scored_tags,
        min_score=min_score,
        max_tags=max_tags,
        max_gap_from_best=0.05
    )
    if selected_tags:
        if is_not_related_to_russia(session, cluster, debug=debug):
            cluster.tags = None
            cluster.status = "not_for_publication"
        else:
            cluster.tags = ", ".join(selected_tags)
            cluster.status = "ready"
    else:
        cluster.tags = None
        cluster.status = "not_for_publication"

    if selected_tags:
        cluster.tags = ", ".join(selected_tags)
        cluster.status = "ready"
    else:
        cluster.tags = None
        cluster.status = "not_for_publication"

    session.commit()

    if debug:
        print("-" * 100)
        print(f"Кластер #{cluster.id}")
        print(f"Заголовок: {cluster.title}")
        print(f"Статус: {cluster.status}")
        print(f"Выбранные теги: {', '.join(selected_tags) if selected_tags else 'не определены'}")
        print("Похожие теги:")

        for item in scored_tags[:8]:
            print(
                f"  {item['tag']}: "
                f"{item['final_score']:.3f} "
                f"(embedding={item['embedding_score']:.3f}, "
                f"boost={item['keyword_boost']:.3f}, "
                f"penalty={item['keyword_penalty']:.3f})"
            )

    return selected_tags



def tag_new_clusters(
    session,
    min_score: float = 0.38,
    max_tags: int = 2,
    debug: bool = True
):
    """
    Тегирует кластеры, у которых уже есть summary.
    Если кластер уже был not_for_publication, можно перетегировать.
    """
    clusters = (
        session.query(Cluster)
        .filter(Cluster.summary_text.isnot(None))
        .filter(
            (Cluster.tags.is_(None)) |
            (Cluster.status == "not_for_publication")
        )
        .all()
    )

    print(f"Кластеров для тегирования: {len(clusters)}")

    for cluster in clusters:
        tag_cluster(
            session=session,
            cluster_id=cluster.id,
            min_score=min_score,
            max_tags=max_tags,
            debug=debug
        )


